# ----------------- if ------------------
x = int(input("please input your want score:"))
if x < 60:
    print("this score very low!")
elif 60 <= x < 70:
    print("just so so!")
elif 70 <= x < 80:
    print("good")
elif 80 <= x < 90:
    print("very good")
elif 90 <= x <= 100:
    print("perfect!")

